function poll_delete(id) {
	if (confirm(gettext('Are you sure you want to delete this poll?')))
		document.location.href='index.php?mod=cms&action=pollDelete&id='+id;
}

function poll_save() {
	document.getElementById('polleditform').submit();
}

function delete_poll_answer(id) {
	if (confirm(gettext('Are you sure you want to delete this answer?')))
		document.location.href='index.php?mod=cms&action=pollDeleteAnswer&id='+id;
}
